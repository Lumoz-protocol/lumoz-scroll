/* eslint-disable node/no-missing-import */
import * as dotenv from "dotenv";
import * as hre from "hardhat";
import { ethers } from "hardhat";
import { selectAddressFile } from "./utils";

dotenv.config();

async function main() {
  const addressFile = selectAddressFile(hre.network.name);

  const [deployer] = await ethers.getSigners();

  const ScrollChain = await ethers.getContractAt("ScrollChain", addressFile.get("ScrollChain.proxy"), deployer);

  const SLOT_ADAPTER = process.env.SLOT_ADAPTER_ADDR || "0x0000000000000000000000000000000000000000";
  const tx = await ScrollChain.setSlotAdapter(SLOT_ADAPTER);
  console.log(" ScrollChain setSlotAdapter, hash:", tx.hash);
  const receipt = await tx.wait();
  console.log(`âœ… Done, gas used: ${receipt.gasUsed}`);
}

// We recommend this pattern to be able to use async/await everywhere
// and properly handle errors.
main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
